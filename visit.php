	<h2>Insurance</h2>
    <h3>Make Your Dental Health the Top Priority</h3>
    <p>Although you may be tempted to decide your dental care based on what insurance will pay, <strong>always remember that your health is the most important thing.</strong>  As with other choices in life - such as buying medical or auto insurance, or even a home - the least expensive option is not always the best.
    </p>
    <p>If your employer offers dental insurance, consider yourself fortunate. This benefit works like a valuable &quot;coupon&quot; that can greatly reduce the costs of dental care. However, no dental benefit plan is set up to cover all your costs.
    </p>
    <p>To avoid surprises on your dental bill, it is important to understand what your insurance will cover and what you will need to cover some other way. <strong>Dental benefits should not be confused with the dental services you need, which are determined by you and your dentist.</strong>
    </p>
    
    <h3>How Dental Plans Work? Why Doesn't My Insurance Pay for This?</h3>
    <p>Almost all dental plans are the result of a contract between your employer and an insurance company. The amount your plan pays is agreed upon by your employer with the insurer.
    </p>
    <p>Your dental coverage is not based on what you need or what your dentist recommends. It is based on how much your employer pays into the plan. Employers generally choose to cover some, but not all, of employees' dental costs. If you are not satisfied with the coverage provided by your insurance, let your employer know.
    </p>
    
    <h3>The Role of Your Dental Office</h3>
    <p>Your dentist's main goal is to help you take good care of your teeth. Our office will file claims with your insurance company as a service to you. The portion of the bill not covered by insurance is your responsibility. While it can be frustrating if you expect insurance to cover the whole bill, the costs of dental care can be managed.  We accept most major credit cards as well as Care Credit. Please ask about the different ways we can assist you in paying your part of the bill. Just get familiar with your dental coverage and do some advance planning. Work with your dentist to take the best possible care of your teeth so they can last a lifetime!</p>
