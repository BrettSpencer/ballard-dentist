<h3>What is Tooth Whitening?</h3>
<p>Tooth Whitening is a cost effective and safe procedure to create a beautiful, healthy smile.  Over the years, flouride is has been added to the whitening product. This reduces the risk of tooth and gum sensitivity. Tooth Whitening must be monitored by your dentist and only done after a comprehensive exam and hygiene cleaning.
</p>

<p>The whitening process can last for a number of years if maintained properly. Beverages such as coffee, tea, cola and wine will reduce the lasting effect. Remember, if it could stain a white shirt, it will stain your smile!
</p>

<h3>What is Bonding?</h3>
<p>Bonding is a cost effective procedure used to fill gaps in front teeth and to change a tooth’s color. The immediate results are amazing. Within a few hours, you will have a great smile!  Bonding like Tooth Whitening may change color over time due to coffee, tea, cola and wine.
</p>

<h3>What are Porcelain Veneers?</h3>
<p>Porcelain Veneers are thin pieces of porcelain that go directly on your natural teeth. This entire procedure can take as few as two visits.  Veneers change the size, shape and color of a patient’s teeth. This procedure is used to repair fractured teeth, teeth darkened by age or medication, or a crooked smile. Many times, patients ask for Porcelain Veneers to simply feel and look younger with a straighter, whiter smile!
</p>

<h3>What are Crowns?</h3>
<p>Crowns are a permanent cosmetic procedure that covers the entire tooth. It will change the size, shape and color of the teeth in as few as 2 visits.
</p>

<h3>What is a Dental Implant?</h3>
<p>A Dental Implant is a “man-made” replacement for a missing tooth or tooth root. Made from titanium, this screw-like object is inserted under the gum and directly into the upper or lower jaw bone. There is usually minimal discomfort involved with this procedure.  After a period of a few months, the Dental Implant and the bone fuse together. This creates an anchor for the new tooth to be placed onto the dental implant.
</p>

<h3>What are the Benefits of Dental Implants?</h3>
	<ul>
    	<li>Dental Implants look and function like your natural tooth.</li>
        <li>Dental Implants are a permanent solution for missing teeth.</li>
        <li>Dental Implants are maintained by routine hygiene visits to your dental office.</li>
        <li>Dental Implants decrease the possibility of bone loss, periodontal disease, tooth movement, and further tooth loss.</li>
        <li>Dental Implants replace the need for a Removable Full or Partial Denture.</li>
        <li>Dental Implants focus only on the tooth or teeth that are missing. A traditional bridge would involve the two or more adjacent teeth being compromised to create a false tooth in between.</li>
    </ul>

<h3>Who is a candidate for Dental Implants?</h3>
<p>With major advancements in Dentistry and Dental Implants, most people are candidates for Dental Implants. There may be exceptions due to chronic illness, heart disease, and severe osteoporosis. What does the Dental Implant Procedure Involve?
</p>

<p>The average Dental Implant Procedure takes 3-4 visits. The first visit is to x-ray the area and take an impression for a surgical guide and a temporary prosthesis to cover the Implant.
</p>

<p>The next visit is to place the Implant. A local anesthesia is applied to the area. Any additional sedation is no longer necessary unless deemed by the dentist. The dentist will then make a minor incision to place the implant. The implant is placed into the jaw bone. The area will then be covered with sutures. The procedure is usually completed with minor pain.
</p>

<p>You will next return in approximately 3 months to begin creating the Porcelain Crown to place over the Implant.
</p>


