    </p>
    <h4>What is Sedation Dentistry or Sleep Dentistry?</h4>
    <p>The patient is given oral medication about one hour before your appointment. The medicine puts you in a light state of sedation and you sleep thru your dental appointment. <strong>It is Safe!</strong> When you awaken your dental treatment is completed.
    </p>
    <p>You need someone to drive you to and from your appointment. Once you arrive, you are allowed to rest comfortably in a warm, quiet room under a cozy blanket. During the entire time you are here, we will closely monitor you to ensure your safety.
    </p>
    <p>Complex dental treatments that often require six or more appointments can be done in as little as one!
    </p>
    <h4>All while you sleep.</h4>
    
    <h3>Oral Conscious Sedation</h3>
    <p>If the idea of going to the dentist for a procedure brings on feelings of fear or anxiety there are options to ease the anxiety. Oral conscious sedation is as easy as taking an aspirin and you will feel relaxed and at ease. Oral conscious sedation does not "knock you out" like some other forms of anesthesia. You will remain awake during your procedure, but you will be in a heightened state of relaxation. It is also possible that you will not remember part, or all, of your procedure.
    </p>
    <p>Oral conscious sedation is extremely safe and easy. It is much less expensive than other forms of sedation, and you will be able to talk and breathe on your own during the procedure. It also makes long or complex procedures seem shorter, allowing you to have all of your work done in a single visit. As an added precaution a local anesthetic will be given to the area that is being worked on to ensure that there is no pain. You will need to have someone drive you to and from the office if you use oral conscious sedation for your safety.
    </p>
    <p>Don't wait any longer to have necessary dental work performed because you are nervous. Ask your dentist about oral conscious sedation to see if it is right for you.
    </p>
    
    <h3>Nitrous Oxide</h3>
    <p>Nitrous Oxide, known to many as laughing gas, is the most commonly used form of sedation in dentistry. It is administered by having the patient inhale the gas, which is carefully monitored by your doctor. You will fall into a euphoric state and feel completely relaxed. All feelings of fear and anxiety fade away, leaving you to sit back peacefully while your dental procedure is performed. In most cases all dental work can be completed in one visit while you are sedated.
    </p>
    <p>Nitrous oxide sedation is very safe and effective. Once the procedure is over the nitrous oxide will wear off fairly quickly, and you will probably be able to drive yourself home and return to your daily activities.
    </p>
    <p>You will need to have a preliminary visit with your dentist to make sure that you are an eligible candidate for nitrous sedation. If you have been worried about your next dental visit its time to stop! Ask us about sedation dentistry and schedule your appointment today.
    </p>
    
    <h3>Digital X-Ray</h3>
    <p>Digital x-rays are quickly becoming adopted by a large percentage of the dental industry. A digital x-ray allows the dentist to take an image of the tooth or teeth and put it into an imaging program. Within this imaging program, there are a number of tools that will allow the dentist to take a very close look at the teeth and surrounding structures with amazing accuracy. As a benefit to the patient, the digital x-ray also provides nearly 80% less radiation than a standard x-ray. This is due to the fact that the digital version of the x-ray is much more sensitive to this radiation and has been specifically designed with the patient in mind.
    </p>
    
    <h3>Oral Cancer Screenings</h3>
    <p>Oral cancer screenings are a very important part of the dental visit for the patient. With the advances in modern technology, we are now able to pinpoint the start of a potential problem much earlier in its evolution. The ability to do so is extremely important in being able to treat any issues prior to them becoming a major irreversible problem.
    </p>
    <p>The oral cancer screening is often completed with an ultraviolet light or similar device that allows us to view issues that can't always be detected by the human eye under normal conditions.
    </p>
    <p>Certain lifestyle choices can have a great impact on the health of tissues and your overall health in the mouth. If you are a smoker or heavy drinker, make sure to get regular screenings when you visit the dentist.
    </p>
    
    <h3>Intraoral Camera</h3>
    <p>The intraoral camera is an amazing diagnostic tool for viewing different angles in the mouth that we would not have been able to just a few years ago. The camera gives us the ability to view the entire mouth on a monitor so that we can get a closer look at any potential issues or problems that may arise. In addition, the patient will have the ability to see for themselves first hand the same images we are seeing. These digital images are also excellent for gaining procedure acceptance from insurance companies.
    </p>
    
    <h3>Rotary Endodontics</h3>
    <p>Endodontics, more commonly known as root canal, is a procedure where the roots or nerves of the tooth are removed. This is often due to the fact that they are infected and the tooth is causing pain.
    </p>
    <p>Rotary Endodontics is a way of performing the root canal utilizing a specific electrical handpiece. This tool often makes the process faster and allows the dentist to perform the process with greater ease.
    </p>
    
    <h3>Electric Handpieces</h3>
    <p>There are basically two types of handpieces, air-driven and electric. Electric handpieces are much stronger than air-driven due to the fact that a motor is spinning the bur as opposed to air. This allows the doctor to remove decay at a faster pace. This will cut down on the time it takes to complete a procedure.
    </p>
    
