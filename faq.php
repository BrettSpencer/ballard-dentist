<h2>FAQ</h2>

<h3>Why are my teeth sensitive?</h3>
<p>Sensitive teeth often come from the fact that your gums have slightly receded. This recession of the gum line allows the underlying dentin to show through which allows water and food easier access to the sensitive nerve. To manage this, there are a number of toothpastes, gels and even some dental procedures that can be applied. Speak to us in more detail if you have very sensitive teeth.
</p>

<h3>What should I do to prevent gum disease and tooth decay?</h3>
<p>Great teeth and gum care start at home. Brushing and flossing on a daily basis is the best way to take care of your teeth and gums on a continual basis.   By keeping to a daily routing you will greatly minimize the risk of gingivitis or tooth decay as you age.
</p>

<h3>What is Gingivitis?</h3>
<p>Gingivitis is a condition caused when bacteria surrounds the teeth and enters the gums.  The gums can become irritated, inflamed and often bleed.  In order to prevent the condition from worsening, regular hygiene visits are highly recommended.  During your visit, our Hygiene team will teach you the proper flossing techniques and Oral Hygiene protocol for Home Care will prevent the Periodontal Disease.
</p>

<h3>What is Periodontal Disease?</h3>
<p>Periodontal Disease is a quiet disease that begins with little or no symptoms. It is caused by bacteria that surrounds the teeth and enters the gums. The immediate condition is known as ‘gingivitis’. The gums become irritated, inflamed and often bleed. If not properly treated, the condition worsens.  Noticeable symptoms now appear. They include:
</p>

	<ul>
    	<li>Bad Breath</li>
        <li>Gum Recession</li>
        <li>Gum Sensitivity to Acidic Foods</li>
        <li>Abscesses</li>
        <li>Tooth Pain</li>
        <li>Tooth Loss</li>
    </ul>

<h3>How Do You Treat Periodontal Disease?</h3>
<p>Periodontal Disease is a chronic condition that needs immediate attention. Through a series of Periodontal Cleanings, Root Planing & Scaling, Laser Therapy and Local Antibiotics, this condition can be controlled. Periodontal Surgery is only necessary for severe cases.
</p>

<h3>What is the Difference Between a White Filling and a Silver Filling?</h3>
<p>Silver Fillings known as Amalgam have been around for decades. Made from a metal alloy, it was the best restoration for fillings. The metal expands and contracts with the heat and cold placed in the mouth. This allowed for little bacteria to enter a tooth once filled; keeping the tooth healthy and strong.
</p>
<p>White Fillings, also known as composites are often made of plastic or glass polymers. These cosmetic fillings allow us to fill a cavity with a substance that will look and feel just like your existing tooth structure. This restoration is created with a resin material and fits tightly into a tooth to prevent decay. Rather than a gray or silver material in your mouth, the composite color will match the tooth color. 
</p>

