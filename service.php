<div id="section9">
	<h2>TMJ</h2>
		<p>TMJ is the acronym for <em>temporomandibular joint</em>, which connects your lower jaw (the mandible) to your skull at the temporal bone. This joint controls many jaw functions, like chewing. If the chewing muscles or the joint itself are causing you pain you may have <em>temporamandibular disorder</em>, or TMD. TMD can be caused by stress, continual clenching of the jaw muscles, or teeth grinding.
        </p>
        <p>Some of the symptoms of TMD are:
        </p>
        <ul>
        	<li>Pain when opening or closing mouth</li>
            <li>Trouble chewing</li>
            <li>Jaw becoming stuck open or shut</li>
            <li>Headaches or ear pain</li>
            <li>Clicking or popping sounds when opening your mouth</li>
            <li>Teeth Grinding</li>
      </ul>
        <p>Many of these symptoms can often be associated with other health problems, so only a medical professional can tell you if it is due to TMD. Teeth grinding is an especially problematic symptom because it can lead to further problems. Prolonged teeth grinding, or bruxism, can cause enamel to wear off teeth and expose dentin. This material is softer than enamel and more susceptible to decay. Sensitivity to hot and cold food or drink may also develop from excessive teeth grinding.
        </p>
        <p>If you suspect you may have TMD come in for a consultation. We can help diagnose you and provide relief for your symptoms. Pain relievers and hot/cold compresses are short term methods to provide relief for pain symptoms. A night guard can be used to help prevent or lessen the effects of teeth grinding at night. This can lead to a more permanent solution. In very severe cases of TMD surgery may be required, but behavioral treatments to change the way you use your jaw muscles are usually enough to provide relief.
        </p>
        
